/**
 * YouTube Page Context Script
 *
 * This script runs in the page context (not extension context)
 * which allows access to YouTube's internal APIs and session data
 */

(function() {
  'use strict';

  console.log('[Helios YouTube Page] Page script loaded and ready');

  // ── POT (Proof of Token) helpers ──
  // Adapted from asbplayer (credit: @kekulta)

  function computeHash(input, start = 0, end = input.length) {
    let hash = 0;
    for (let i = start; i < end; i++) {
      hash = (Math.imul(31, hash) + input.charCodeAt(i)) | 0;
    }
    return hash;
  }

  function transformData(data, videoId) {
    const mid = videoId.length >> 1;
    const key1 = computeHash(videoId, 0, mid);
    const key2 = computeHash(videoId, mid);
    const data32 = new Uint32Array(data.buffer);
    const firstWord = data32[0];

    for (let i = 1; i < data32.length; i += 2) {
      let a = firstWord;
      let b = i;
      let c = key1;
      let d = key2;

      for (let round = 0; round < 22; round++) {
        b = ((b >>> 8) | (b << 24)) + a;
        b ^= c + 38293;
        a = ((a << 3) | (a >>> 29)) ^ b;

        d = ((d >>> 8) | (d << 24)) + c;
        d ^= round + 38293;
        c = ((c << 3) | (c >>> 29)) ^ d;
      }

      data32[i] ^= a;
      if (i + 1 < data32.length) {
        data32[i + 1] ^= b;
      }
    }
  }

  function base64ToUint8Array(base64) {
    const binary = atob(base64);
    const bytes = new Uint8Array(binary.length);
    for (let i = 0; i < binary.length; i++) {
      bytes[i] = binary.charCodeAt(i);
    }
    return bytes;
  }

  function uint8ArrayToBase64(bytes) {
    let binary = '';
    for (let i = 0; i < bytes.length; i++) {
      binary += String.fromCharCode(bytes[i]);
    }
    return btoa(binary);
  }

  function decodeCachedPoToken(videoId, encodedPoToken) {
    try {
      const data = base64ToUint8Array(encodedPoToken);
      transformData(data, videoId);

      let index = 4;
      while (index < 7 && data[index] === 0) {
        index++;
      }

      const VALIDATION_BYTES = [196, 200, 224, 18];
      for (let i = 0; i < VALIDATION_BYTES.length; i++) {
        if (data[index++] !== VALIDATION_BYTES[i]) {
          return null;
        }
      }

      const timestamp = new DataView(data.buffer).getUint32(index);
      index += 4;

      const poToken = uint8ArrayToBase64(new Uint8Array(data.buffer, index))
        .replace(/\//g, '_')
        .replace(/\+/g, '-')
        .replace(/=+$/g, '');

      return { expires: new Date(timestamp * 1000), poToken };
    } catch (e) {
      return null;
    }
  }

  function decodePoToken(videoId) {
    try {
      const potKeyString = window.sessionStorage.getItem('iU5q-!O9@$');
      if (!potKeyString) return null;

      const potKeys = potKeyString.split(',');
      let sessionPoToken = null;

      for (const key of potKeys) {
        const encodedToken = window.sessionStorage.getItem(key);
        if (!encodedToken) continue;

        const decoded = decodeCachedPoToken(videoId, encodedToken);
        if (decoded) {
          sessionPoToken = decoded;
        }
      }
      return sessionPoToken;
    } catch (e) {
      return null;
    }
  }

  async function fetchPlayerContextForPage() {
    try {
      const webResponse = await fetch(window.location.href);
      if (!webResponse.ok) return null;

      const pageString = await webResponse.text();
      const doc = new DOMParser().parseFromString(pageString, 'text/html');
      const scripts = doc.body.querySelectorAll('script');

      for (const elm of scripts) {
        if (elm.textContent && elm.textContent.includes('ytInitialPlayerResponse')) {
          try {
            const context = new Function(`${elm.textContent}; return ytInitialPlayerResponse;`)();
            if (context) return context;
          } catch (e) {
            // Try next script
          }
        }
      }
      return null;
    } catch (e) {
      return null;
    }
  }

  // ── Track formatting helper ──

  function formatTrack(track, url) {
    url.searchParams.set('fmt', 'srv3');
    return {
      language: track.languageCode,
      languageName: track.name?.text || track.name?.simpleText || track.name?.runs?.[0]?.text || track.languageCode,
      url: url.toString(),
      isAutoGenerated: track.kind === 'asr'
    };
  }

  // ── Method 1: Android InnerTube API ──

  async function fetchSubtitlesViaInnerTube(videoId) {
    try {
      if (typeof window.ytcfg?.get !== 'function') {
        console.warn('[Helios YouTube Page] ytcfg not available');
        return null;
      }

      const apiKey = window.ytcfg.get('INNERTUBE_API_KEY');
      if (!apiKey) {
        console.warn('[Helios YouTube Page] No API key found');
        return null;
      }

      const hl = window.ytcfg.get('HL') || 'en';

      const response = await fetch(`https://${window.location.host}/youtubei/v1/player?key=${apiKey}`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          context: {
            client: {
              clientName: 'ANDROID',
              clientVersion: '20.10.38',
              hl: hl,
            },
          },
          videoId: videoId,
        }),
      });

      if (!response.ok) {
        console.warn('[Helios YouTube Page] InnerTube API error:', response.status);
        return null;
      }

      const data = await response.json();
      const captionTracks = data?.captions?.playerCaptionsTracklistRenderer?.captionTracks;

      if (!captionTracks || captionTracks.length === 0) {
        console.warn('[Helios YouTube Page] No caption tracks in InnerTube response');
        return null;
      }

      return captionTracks.map(track => formatTrack(track, new URL(track.baseUrl)));
    } catch (error) {
      console.error('[Helios YouTube Page] InnerTube error:', error);
      return null;
    }
  }

  // ── Method 2: POT fallback (page HTML + session token) ──

  async function fetchSubtitlesViaPot(videoId) {
    try {
      const pot = decodePoToken(videoId);
      if (!pot) {
        console.warn('[Helios YouTube Page] No POT token available');
        return null;
      }

      const playerContext = await fetchPlayerContextForPage();
      if (!playerContext) {
        console.warn('[Helios YouTube Page] Could not extract player context from page');
        return null;
      }

      const captionTracks = playerContext?.captions?.playerCaptionsTracklistRenderer?.captionTracks;
      if (!captionTracks || captionTracks.length === 0) {
        console.warn('[Helios YouTube Page] No caption tracks in player context');
        return null;
      }

      return captionTracks.map(track => {
        const baseUrl = track.baseUrl;
        const fullUrl = baseUrl.startsWith('/') ? `https://${window.location.host}${baseUrl}` : baseUrl;
        const url = new URL(fullUrl);
        url.searchParams.set('pot', pot.poToken);
        url.searchParams.set('c', 'WEB');
        return formatTrack(track, url);
      });
    } catch (error) {
      console.error('[Helios YouTube Page] POT fallback error:', error);
      return null;
    }
  }

  // ── Video ID extraction ──

  function getCurrentVideoId() {
    // Try URL parameter
    const urlParams = new URLSearchParams(window.location.search);
    const videoId = urlParams.get('v');
    if (videoId) return videoId;

    // Try shorts/embed path
    const pathMatch = /\/(shorts|embed)\/([^/?#]+)/.exec(window.location.pathname);
    if (pathMatch) return pathMatch[2];

    // Try ytInitialData
    if (window.ytInitialData?.currentVideoEndpoint?.watchEndpoint?.videoId) {
      return window.ytInitialData.currentVideoEndpoint.watchEndpoint.videoId;
    }

    return null;
  }

  // ── Main handler: try InnerTube first, then POT fallback ──

  window.addEventListener('helios-youtube-request-subtitles', async (event) => {
    console.log('[Helios YouTube Page] Received subtitle request for video:', event.detail?.videoId);
    const videoId = event.detail?.videoId || getCurrentVideoId();

    if (!videoId) {
      console.error('[Helios YouTube Page] No video ID available');
      window.dispatchEvent(new CustomEvent('helios-youtube-subtitles-response', {
        detail: { success: false, error: 'No video ID' }
      }));
      return;
    }

    // Method 1: Android InnerTube API
    let tracks = await fetchSubtitlesViaInnerTube(videoId);

    // Method 2: POT fallback
    if (!tracks) {
      console.log('[Helios YouTube Page] InnerTube failed, trying POT fallback...');
      tracks = await fetchSubtitlesViaPot(videoId);
    }

    if (tracks && tracks.length > 0) {
      console.log('[Helios YouTube Page] Sending', tracks.length, 'subtitle tracks to content script');
      window.dispatchEvent(new CustomEvent('helios-youtube-subtitles-response', {
        detail: { success: true, tracks: tracks }
      }));
    } else {
      console.warn('[Helios YouTube Page] All methods failed to fetch tracks');
      window.dispatchEvent(new CustomEvent('helios-youtube-subtitles-response', {
        detail: { success: false, error: 'Failed to fetch tracks' }
      }));
    }
  });

  // Auto-fetch on page load
  setTimeout(() => {
    const videoId = getCurrentVideoId();
    if (videoId) {
      window.dispatchEvent(new CustomEvent('helios-youtube-request-subtitles', {
        detail: { videoId: videoId }
      }));
    }
  }, 2000);
})();
