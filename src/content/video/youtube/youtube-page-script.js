/**
 * YouTube Page Context Script
 *
 * This script runs in the page context (not extension context)
 * which allows access to YouTube's internal APIs and session data
 */

(function() {
  'use strict';

  console.log('[Helios YouTube Page] Page script loaded');

  /**
   * Fetch subtitle tracks using Android InnerTube API
   * This bypasses YouTube's bot protection by pretending to be Android client
   */
  async function fetchSubtitlesViaInnerTube(videoId) {
    try {
      // Check if YouTube config is available
      if (typeof window.ytcfg?.get !== 'function') {
        console.warn('[Helios YouTube Page] ytcfg not available');
        return null;
      }

      // Get API key from YouTube config
      const apiKey = window.ytcfg.get('INNERTUBE_API_KEY');
      if (!apiKey) {
        console.warn('[Helios YouTube Page] No API key found');
        return null;
      }

      const hl = window.ytcfg.get('HL') || 'en';

      console.log('[Helios YouTube Page] Fetching subtitles via InnerTube API');
      console.log('[Helios YouTube Page] Video ID:', videoId);
      console.log('[Helios YouTube Page] Language:', hl);

      // Call YouTube's InnerTube API as Android client
      const response = await fetch(`https://${window.location.host}/youtubei/v1/player?key=${apiKey}`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          context: {
            client: {
              clientName: 'ANDROID',       // Critical: Android client bypasses bot protection
              clientVersion: '19.09.36',
              hl: hl,
            },
          },
          videoId: videoId,
        }),
      });

      if (!response.ok) {
        console.error('[Helios YouTube Page] InnerTube API error:', response.status);
        return null;
      }

      const data = await response.json();

      // Extract caption tracks
      const captionTracks = data?.captions?.playerCaptionsTracklistRenderer?.captionTracks;

      if (!captionTracks || captionTracks.length === 0) {
        console.warn('[Helios YouTube Page] No caption tracks found');
        return null;
      }

      console.log('[Helios YouTube Page] Found', captionTracks.length, 'caption tracks');

      // Format tracks for content script
      const tracks = captionTracks.map(track => {
        // Add format parameter to URL
        const url = new URL(track.baseUrl);
        url.searchParams.set('fmt', 'srv3'); // Request srv3 XML format

        return {
          language: track.languageCode,
          languageName: track.name?.simpleText || track.languageCode,
          url: url.toString(),
          isAutoGenerated: track.kind === 'asr'
        };
      });

      return tracks;
    } catch (error) {
      console.error('[Helios YouTube Page] Error fetching subtitles:', error);
      return null;
    }
  }

  /**
   * Get current video ID from page
   */
  function getCurrentVideoId() {
    // Try URL parameter
    const urlParams = new URLSearchParams(window.location.search);
    const videoId = urlParams.get('v');

    if (videoId) {
      return videoId;
    }

    // Try ytInitialData
    if (window.ytInitialData?.currentVideoEndpoint?.watchEndpoint?.videoId) {
      return window.ytInitialData.currentVideoEndpoint.watchEndpoint.videoId;
    }

    return null;
  }

  /**
   * Listen for requests from content script
   */
  window.addEventListener('helios-youtube-request-subtitles', async (event) => {
    console.log('[Helios YouTube Page] Received subtitle request');

    const videoId = event.detail?.videoId || getCurrentVideoId();

    if (!videoId) {
      console.error('[Helios YouTube Page] No video ID available');
      window.dispatchEvent(new CustomEvent('helios-youtube-subtitles-response', {
        detail: { success: false, error: 'No video ID' }
      }));
      return;
    }

    const tracks = await fetchSubtitlesViaInnerTube(videoId);

    if (tracks) {
      console.log('[Helios YouTube Page] Sending', tracks.length, 'tracks to content script');
      window.dispatchEvent(new CustomEvent('helios-youtube-subtitles-response', {
        detail: { success: true, tracks: tracks }
      }));
    } else {
      window.dispatchEvent(new CustomEvent('helios-youtube-subtitles-response', {
        detail: { success: false, error: 'Failed to fetch tracks' }
      }));
    }
  });

  // Auto-fetch on page load
  setTimeout(() => {
    const videoId = getCurrentVideoId();
    if (videoId) {
      console.log('[Helios YouTube Page] Auto-fetching subtitles for video:', videoId);
      window.dispatchEvent(new CustomEvent('helios-youtube-request-subtitles', {
        detail: { videoId: videoId }
      }));
    }
  }, 2000);

  console.log('[Helios YouTube Page] Ready to fetch subtitles');
})();