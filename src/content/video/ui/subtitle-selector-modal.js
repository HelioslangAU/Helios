/**
 * Subtitle Selector Modal (ASB Player style)
 * Shows available subtitle tracks for user to select
 */
class SubtitleSelectorModal {
  constructor() {
    this.modal = null;
    this.tracks = [];
    this.onTrackSelected = null; // Callback
    this.isVisible = false;
  }

  /**
   * Show modal with available tracks
   * @param {Array} tracks - Available subtitle tracks
   * @param {Function} onSelect - Callback when track selected
   * @param {Object} currentTrack - Currently selected track (optional)
   */
  show(tracks, onSelect, currentTrack = null) {
    this.tracks = tracks;
    this.onTrackSelected = onSelect;
    this.currentTrack = currentTrack;

    if (!this.modal) {
      this._createModal();
    }

    this._renderTracks();
    this.modal.style.display = 'flex';
    this.isVisible = true;
  }

  /**
   * Hide modal
   */
  hide() {
    if (this.modal) {
      this.modal.style.display = 'none';
      this.isVisible = false;
    }
  }

  /**
   * Create modal UI
   */
  _createModal() {
    this.modal = document.createElement('div');
    this.modal.className = 'helios-subtitle-selector-modal';
    this.modal.innerHTML = `
      <div class="helios-subtitle-selector-content">
        <div class="helios-subtitle-selector-header">
          <h3>Select Caption Track</h3>
          <button class="helios-subtitle-selector-close">&times;</button>
        </div>
        <div class="helios-subtitle-selector-list"></div>
        <div class="helios-subtitle-selector-footer">
          <button class="helios-subtitle-selector-import" title="Import your own subtitle file (SRT, VTT)">
            <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
              <path d="M21 15v4a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2v-4"></path>
              <polyline points="7 10 12 15 17 10"></polyline>
              <line x1="12" y1="15" x2="12" y2="3"></line>
            </svg>
            Import File
          </button>
          <div class="helios-subtitle-selector-actions">
            <button class="helios-subtitle-selector-cancel">Cancel</button>
            <button class="helios-subtitle-selector-ok">Select</button>
          </div>
        </div>
      </div>
    `;

    document.body.appendChild(this.modal);
    this._setupEventListeners();
  }

  /**
   * Setup event listeners
   */
  _setupEventListeners() {
    // Close button
    const closeBtn = this.modal.querySelector('.helios-subtitle-selector-close');
    closeBtn.addEventListener('click', () => this.hide());

    // Cancel button
    const cancelBtn = this.modal.querySelector('.helios-subtitle-selector-cancel');
    cancelBtn.addEventListener('click', () => this.hide());

    // OK button
    const okBtn = this.modal.querySelector('.helios-subtitle-selector-ok');
    okBtn.addEventListener('click', () => this._handleSelect());

    // Import button
    const importBtn = this.modal.querySelector('.helios-subtitle-selector-import');
    importBtn.addEventListener('click', () => this._handleImport());

    // Click outside to close
    this.modal.addEventListener('click', (e) => {
      if (e.target === this.modal) {
        this.hide();
      }
    });

    // ESC key to close
    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape' && this.isVisible) {
        this.hide();
      }
    });
  }

  /**
   * Render available tracks
   */
  _renderTracks() {
    const list = this.modal.querySelector('.helios-subtitle-selector-list');
    list.innerHTML = '';

    if (this.tracks.length === 0) {
      list.innerHTML = '<div class="helios-subtitle-selector-empty">No subtitle tracks available</div>';
      return;
    }

    // Debug log to see what currentTrack contains
    console.log('[Helios Subtitle Selector] Current track:', this.currentTrack);
    console.log('[Helios Subtitle Selector] All tracks:', this.tracks);

    this.tracks.forEach((track, index) => {
      console.log(`[Helios Subtitle Selector] Checking track ${index}:`, {
        id: track.id,
        language: track.language,
        name: track.languageName,
        isCC: track.isClosedCaptions,
        isAuto: track.isAutoGenerated,
        isForced: track.isForcedNarrative
      });
      const item = document.createElement('div');
      item.className = 'helios-subtitle-selector-item';

      // Check if this is the current track
      let isCurrentTrack = false;

      if (this.currentTrack) {
        // For Netflix: Use unique trackId for exact matching
        if (track.id && this.currentTrack.id) {
          isCurrentTrack = track.id === this.currentTrack.id;
        }
        // For YouTube: Use URL + isAutoGenerated flag
        else if (track.url && this.currentTrack.url) {
          isCurrentTrack = track.url === this.currentTrack.url &&
                          (track.isAutoGenerated || false) === (this.currentTrack.isAutoGenerated || false);
        }
        // Note: No fallback to language matching - IDs/URLs are unique identifiers
      }

      if (isCurrentTrack) {
        item.classList.add('selected');
      }

      item.innerHTML = `
        <input type="radio" name="subtitle-track" id="track-${index}" value="${index}" ${isCurrentTrack ? 'checked' : ''}>
        <label for="track-${index}">
          <span class="track-name">${track.languageName || track.language}</span>
          <div class="track-badges">
            ${track.isAutoGenerated ? '<span class="track-badge">Auto-generated</span>' : ''}
            ${isCurrentTrack ? '<span class="track-badge track-badge-current">CURRENT</span>' : ''}
          </div>
        </label>
      `;

      // Make entire item clickable to select radio
      item.addEventListener('click', (e) => {
        // Don't interfere if clicking directly on radio or label
        if (e.target.tagName !== 'INPUT' && e.target.tagName !== 'LABEL') {
          const radio = item.querySelector('input[type="radio"]');
          if (radio) {
            radio.checked = true;
            // Update selected class
            list.querySelectorAll('.helios-subtitle-selector-item').forEach(i => i.classList.remove('selected'));
            item.classList.add('selected');
          }
        }
      });

      // Update selected class on radio change
      const radio = item.querySelector('input[type="radio"]');
      radio.addEventListener('change', () => {
        list.querySelectorAll('.helios-subtitle-selector-item').forEach(i => i.classList.remove('selected'));
        item.classList.add('selected');
      });

      list.appendChild(item);
    });
  }

  /**
   * Handle Select button click
   */
  _handleSelect() {
    const list = this.modal.querySelector('.helios-subtitle-selector-list');
    const selected = list.querySelector('input[name="subtitle-track"]:checked');
    if (selected && this.onTrackSelected) {
      const trackIndex = parseInt(selected.value);
      const track = this.tracks[trackIndex];
      this.onTrackSelected(track);
    }
    this.hide();
  }

  /**
   * Handle Import button click
   */
  _handleImport() {
    // Check if file loader is available
    if (!window.heliosVideoFeature?.fileLoader) {
      console.error('[Helios Subtitle Selector] File loader not available');
      // Dispatch notification event that sidebar can catch
      document.dispatchEvent(new CustomEvent('helios-video-notification', {
        detail: { message: 'Subtitle import not available', type: 'error' }
      }));
      return;
    }

    // Hide the modal first
    this.hide();

    // Show notification to guide user
    document.dispatchEvent(new CustomEvent('helios-video-notification', {
      detail: { message: 'Select a subtitle file (SRT or VTT)', type: 'info' }
    }));

    // Trigger the file picker
    window.heliosVideoFeature.fileLoader.openFilePicker();
  }

  /**
   * Destroy modal
   */
  destroy() {
    if (this.modal && this.modal.parentElement) {
      this.modal.parentElement.removeChild(this.modal);
    }
    this.modal = null;
  }
}
